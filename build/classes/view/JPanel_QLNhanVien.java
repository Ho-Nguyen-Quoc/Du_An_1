        this.txtHoTen.setText(null);
        this.cldNgaySinh.setCalendar(null);
        this.txtDiaChi.setText(null);
        this.txtSoDT.setText(null);
        this.txtEmail.setText(null);
        this.cboChucVu.setSelectedIndex(0);
        this.lblAnh.setText("150 x 228");
        this.lblAnh.setIcon(null);
        // Cập nhật trạng thái
