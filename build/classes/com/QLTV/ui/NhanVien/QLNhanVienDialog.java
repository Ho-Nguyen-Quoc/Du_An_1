    void delete() {
        String manv = txtmanv.getText();
        try {
            dao.delete(manv);
            this.filltable();
            this.clearForm();
        } catch (Exception e) {
        }
    }
    void update() {
        NhanVien model = getModel();
       
            try {
                dao.update(model);
                this.filltable();
                this.clearForm();
                MsgBox.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại!");
            }
        
    }
